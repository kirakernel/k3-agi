def main():
	x = int(input('what\'s x? '))
	y = int(input('what\'s y? '))

	print(f'{x} + {y} = {x + y}')
	print(f'{x} - {y} = {x - y}')
	print(f'{x} * {y} = {x * y}')
	print(f'{x} ** {y} = {x ** y}')
	print(f'{x} / {y} = {x / y}')
	print(f'{x} // {y} = {x // y}')
	print(f'{x} % {y} = {x % y}')


if __name__ == "__main__":
	main()